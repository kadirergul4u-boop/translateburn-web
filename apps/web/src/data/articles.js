export function getArticleBySlug(slug) {
  return articles.find(article => article.slug === slug);
}

export function getRelatedArticles(currentArticleId, relatedIds) {
  return articles.filter(article => relatedIds.includes(article.id));
}

export function getAllArticles() {
  return articles.sort((a, b) => new Date(b.tr.publishDate) - new Date(a.tr.publishDate));
}

export function searchArticles(query, lang = 'tr') {
  const lowerQuery = query.toLowerCase();
  return articles.filter(article => 
    article[lang].title.toLowerCase().includes(lowerQuery) ||
    article[lang].excerpt.toLowerCase().includes(lowerQuery) ||
    article[lang].content.toLowerCase().includes(lowerQuery) ||
    article[lang].keywords.some(keyword => keyword.toLowerCase().includes(lowerQuery))
  );
}

export function getArticlesByCategory(category, lang = 'tr') {
  return articles.filter(article => article[lang].category === category);
}
